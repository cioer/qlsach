    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        String current = btAdd.getText();
        if (current.equals("Thêm")) {
            btAdd.setText("Lưu");
            showTextField(true);
            tfMaS.setText(sCtl.createMaSach());
            btDelete.setEnabled(false);
            btFix.setEnabled(false);

        } else {

            String mas = gt(tfMaS);
            String tens = gt(tfTenS);
            String tacgia = gt(tfTacgia);
            String namxb = gt(tfNamXB);
            Float dongia = Float.valueOf(gt(tfDongia));
            String manxb = gt(cmbNXB);
            Sach s = new Sach(mas, tens, tacgia, namxb, dongia, manxb);
            s.add();
            sCtl.bookToTable(tb);
            resetTextFields();
            btAdd.setText("Thêm");
        }
    }//GEN-LAST:event_btAddActionPerformed

