    private void resetTextFields() {
        btAdd.setEnabled(true);
        btFix.setEnabled(true);
        btDelete.setEnabled(true);
        tfDongia.setText("");
        tfMaS.setText("");
        tfNamXB.setText("");
        tfTacgia.setText("");
        tfTenS.setText("");

    }

    private String gt(JTextField tf) {
        return tf.getText().trim();
    }

    private String gt(JComboBox cmb) {
        return cmb.getSelectedItem().toString();
    }

